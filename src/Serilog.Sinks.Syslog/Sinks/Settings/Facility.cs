namespace Serilog.Sinks.Syslog
{
    /// <summary>
    /// A value used to categorize the system or application that generated the syslog event.
    /// </summary>
    public enum Facility
    {
        /// <summary>
        /// Kernel messages
        /// </summary>
        Kernel = 0,

        /// <summary>
        /// User-level messages
        /// </summary>
        User = 1,

        /// <summary>
        /// Mail system
        /// </summary>
        Mail = 2,

        /// <summary>
        /// System daemons
        /// </summary>
        Daemons = 3,

        /// <summary>
        /// Security/authorisation messages
        /// </summary>
        Auth = 4,

        /// <summary>
        /// Messages generated by the syslog daemon itself
        /// </summary>
        Syslog = 5,

        /// <summary>
        /// Line printer subsystem
        /// </summary>
        LPR = 6,

        /// <summary>
        /// Network news subsystem
        /// </summary>
        News = 7,

        /// <summary>
        /// UUCP subsystem
        /// </summary>
        UUCP = 8,

        /// <summary>
        /// Cron scheduler daemon
        /// </summary>
        Cron = 9,

        /// <summary>
        /// Security/authorisation messages (private)
        /// </summary>
        Auth2 = 10,

        /// <summary>
        /// FTP daemon
        /// </summary>
        FTP = 11,

        /// <summary>
        /// NTP subsystem
        /// </summary>
        NTP = 12,

        /// <summary>
        /// Log audit
        /// </summary>
        LogAudit = 13,

        /// <summary>
        /// Log alert
        /// </summary>
        LogAlert = 14,

        /// <summary>
        /// Cron scheduler daemon
        /// </summary>
        Cron2 = 15,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local0 = 16,

        /// <summary>
