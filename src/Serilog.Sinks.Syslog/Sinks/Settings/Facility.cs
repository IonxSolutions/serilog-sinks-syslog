        /// </summary>
        Local2 = 18,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local3 = 19,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local4 = 20,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local5 = 21,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local6 = 22,

        /// <summary>
        /// Reserved for local use (0)
        /// </summary>
        Local7 = 23
    }
}
